package com.swl.catchmovie;

/**
 *
 */

public class Movie {
    String title;
    String image;
    String movieAdvice;
    String movieLanguage;
    String movieDuration;
    String movieDirector;
    String movieCast;
    String movieReleaseDate;
    String movieDistributor;
    String movieSynopsis;

    public String getMovieAdvice() {
        return movieAdvice;
    }

    public void setMovieAdvice(String movieAdvice) {
        this.movieAdvice = movieAdvice;
    }

    public String getMovieLanguage() {
        return movieLanguage;
    }

    public void setMovieLanguage(String movieLanguage) {
        this.movieLanguage = movieLanguage;
    }

    public String getMovieDuration() {
        return movieDuration;
    }

    public void setMovieDuration(String movieDuration) {
        this.movieDuration = movieDuration;
    }

    public String getMovieDirector() {
        return movieDirector;
    }

    public void setMovieDirector(String movieDirector) {
        this.movieDirector = movieDirector;
    }

    public String getMovieCast() {
        return movieCast;
    }

    public void setMovieCast(String movieCast) {
        this.movieCast = movieCast;
    }

    public String getMovieReleaseDate() {
        return movieReleaseDate;
    }

    public void setMovieReleaseDate(String movieReleaseDate) {
        this.movieReleaseDate = movieReleaseDate;
    }

    public String getMovieDistributor() {
        return movieDistributor;
    }

    public void setMovieDistributor(String movieDistributor) {
        this.movieDistributor = movieDistributor;
    }

    public String getMovieSynopsis() {
        return movieSynopsis;
    }

    public void setMovieSynopsis(String movieSynopsis) {
        this.movieSynopsis = movieSynopsis;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }


}
